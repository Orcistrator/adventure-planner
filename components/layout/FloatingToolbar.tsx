const CHOMP_TRANSITION = {
  duration: 0.28,
  repeat: Infinity,
  repeatType: "mirror" as const,
  ease: "easeInOut" as const,
};

const RESET_TRANSITION = {
  duration: 0.35,
  ease: [0.23, 1, 0.32, 1] as const,
};

export default function FloatingToolbar() {
  const pathname = usePathname();
  const [logoHovered, setLogoHovered] = useState(false);

  return (
    <div className="fixed left-4 top-4 z-50 flex flex-col w-12 rounded-[120px] bg-stone-900">
      {/* Logo — links home */}
      <Link
        href="/"
        className="flex h-12 items-center justify-center shrink-0"
        onMouseEnter={() => setLogoHovered(true)}
        onMouseLeave={() => setLogoHovered(false)}
        title="Home"
      >
        <svg
          viewBox="0 0 24 18"
          fill="none"
          xmlns="http://www.w3.org/2000/svg"
          className="text-stone-500"
          style={{ width: 24, height: "auto", flexShrink: 0 }}
        >
          {LOGO_PATHS.resting.map((restingPath, i) => (
            <motion.path
              key={i}
              fill="currentColor"
              initial={{ d: restingPath }}
              animate={{
                d: logoHovered
                  ? [restingPath, LOGO_PATHS.hover[i]]
                  : restingPath,
              }}
              transition={logoHovered ? CHOMP_TRANSITION : RESET_TRANSITION}
            />
          ))}
        </svg>
      </Link>

      {/* Nav items */}
      <div className="flex flex-col items-center gap-1 pb-2">
        {navItems.map(({ href, label, icon: Icon }) => {
          const isActive =
            href === "/" ? pathname === "/" : pathname.startsWith(href);
          return (
            <Link
              key={href}
              href={href}
              title={label}
              className={`flex items-center justify-center rounded-full size-8 transition-[background-color,transform] duration-150 ease-out active:scale-95 ${
                isActive ? "bg-stone-700/50" : "hover:bg-stone-700/35"
              }`}
            >
              <Icon
                size={16}
                strokeWidth={1.5}
                className={isActive ? "text-stone-100" : "text-stone-400"}
              />
            </Link>
          );
        })}
      </div>
    </div>
  );
}
